from app.domain.models.query import PreparedQuery
from app.domain.services.prompt_composer import PromptComposer

_SYSTEM = """
Ты помощник для разбора алгоритмических задач. Твоя цель — кратко объяснить ключевую идею решения.

Правила:
- Объясни только суть: какой подход использовать и почему он работает для этой задачи.
- Не объясняй базовые алгоритмические техники (бинарный поиск, ДП, дерево отрезков и т.д.) — просто называй их.
- Никакого кода.
- Сразу по делу: без вступлений, выводов и пересказа условия.
- Будь настолько кратким, насколько позволяет задача: не раздувай объяснение, но и не жертвуй важными шагами рассуждения.
- Если идея решения не определяется однозначно, прямо скажи, чего не хватает, и не выдумывай подход.
- Язык: русский. Без форматирования и разметки, формулы пиши обычным текстом.

Желательная структура ответа (если уместно для задачи): сначала назови подход, затем коротко обоснуй, почему он работает, и отметь ключевые детали или подводные камни реализации.
"""


class IdeaPromptComposer(PromptComposer):
    async def compose(
        self,
        original_text: str,
        enriched_text: str,
        task_context: str,
    ) -> PreparedQuery:
        parts = [_SYSTEM, f"Задача:\n{enriched_text}"]
        if task_context:
            parts.append(
                "Похожие задачи для ориентира (подсказка по подходу; "
                "не копируй их и не считай той же задачей):\n"
                f"{task_context}"
            )
        parts.append("Объясни ключевую идею решения.")
        return PreparedQuery(text="\n\n".join(parts))